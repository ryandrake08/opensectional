// return a color suitable to pass to glColor3ubv() or glColor4ubv()
const unsigned char* color::buffer() const
{
    return &this->buf.front();
}

// return components suitable to pass to glClearColor()
float color::r_f() const
{
    return this->buf[0] / 255.0F;
}

float color::g_f() const
{
    return this->buf[1] / 255.0F;
}

float color::b_f() const
{
    return this->buf[2] / 255.0F;
}

float color::a_f() const
{
    return this->buf[3] / 255.0F;
}

// return components suitable to pass to C4UB vertex buffers
unsigned char color::r() const
{
    return this->buf[0];
}

unsigned char color::g() const
{
    return this->buf[1];
}

unsigned char color::b() const
{
    return this->buf[2];
}

unsigned char color::a() const
{
    return this->buf[3];
}

// linear interpolation between two colors
color color::lerp(const color& c0, const color& c1, float factor1)
{
    const float factor0 = 1.0F - factor1;

    return color(static_cast<unsigned char>(c0.buf[0] * factor0 + c1.buf[0] * factor1),
                 static_cast<unsigned char>(c0.buf[1] * factor0 + c1.buf[1] * factor1),
                 static_cast<unsigned char>(c0.buf[2] * factor0 + c1.buf[2] * factor1),
                 static_cast<unsigned char>(c0.buf[3] * factor0 + c1.buf[3] * factor1));
}

// darken color
color color::operator*(float factor) const
{
    return color(static_cast<unsigned char>(this->buf[0] * factor),
                 static_cast<unsigned char>(this->buf[1] * factor),
                 static_cast<unsigned char>(this->buf[2] * factor),
                 this->buf[3]);
}
