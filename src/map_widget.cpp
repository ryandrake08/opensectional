void map_widget::button_event(sdl::input_button_t button, sdl::input_action_t action, sdl::input_mod_t)
{
    // Track button state for drag detection.
    if(action == sdl::input_action::release)
        pimpl->buttons_down.erase(button);
    else
        pimpl->buttons_down.insert(button);

    if(button.value != BUTTON_LEFT)
        return;

    if(action.value != 0) // press
    {
        pimpl->dragged = false;
    }
    else // release
    {
        if(!pimpl->dragged && !pimpl->imgui_wants_mouse)
        {
            pimpl->handle_pick();
        }
    }
}

bool map_widget::draw_imgui()
{
    // Both popups can coexist in theory, but in practice the selector is
    // dismissed as soon as a feature is chosen. Always draw both so either
    // one's warmup frames propagate upward.
    bool a = pimpl->draw_pick_imgui();
    bool b = pimpl->draw_info_imgui();
    return a || b;
}

void map_widget::cursor_position_event(double xpos, double ypos)
{
    // Convert from window pixels to NDC via the projection matrix.
    double ypos_flipped = pimpl->viewport_vec.w - ypos;
    glm::vec3 pixel_pos(xpos, ypos_flipped, 0.0);
    glm::vec3 pos(glm::unProject(pixel_pos, glm::mat4(1.0F),
                                  pimpl->projection_matrix, pimpl->viewport_vec));

    pimpl->cursor_ndc_x = pos[0];
    pimpl->cursor_ndc_y = pos[1];

    // If any button is held, dispatch as a drag.
    if(!pimpl->buttons_down.empty())
    {
        double dx = pos[0] - pimpl->cursor_last_x;
        double dy = pos[1] - pimpl->cursor_last_y;
        pimpl->dragged = true;
        double dx_meters = -dx * pimpl->view.half_extent_y * 2.0;
        double dy_meters = -dy * pimpl->view.half_extent_y * 2.0;
        pimpl->view.pan_meters(dx_meters, dy_meters);
        pimpl->rebuild_grid();
        pimpl->update_tiles();
    }

    pimpl->cursor_last_x = pos[0];
    pimpl->cursor_last_y = pos[1];
}

void map_widget::key_event(sdl::input_key_t key, sdl::input_action_t action, sdl::input_mod_t)
{
    if(action != sdl::input_action::release)
    {
        switch(key.value)
        {
        case 'w':
        case 'W':
            pimpl->view.pan(0.0, 0.1);
            pimpl->rebuild_grid();
            pimpl->update_tiles();
            break;
        case 's':
        case 'S':
            pimpl->view.pan(0.0, -0.1);
            pimpl->rebuild_grid();
            pimpl->update_tiles();
            break;
        case 'a':
        case 'A':
            pimpl->view.pan(-0.1, 0.0);
            pimpl->rebuild_grid();
            pimpl->update_tiles();
            break;
        case 'd':
        case 'D':
            pimpl->view.pan(0.1, 0.0);
            pimpl->rebuild_grid();
            pimpl->update_tiles();
            break;
        case 'r':
        case 'R':
        {
            int z = static_cast<int>(pimpl->view.zoom_level()) + 1;
            pimpl->view.zoom_to_level(z);
            pimpl->rebuild_grid();
            pimpl->update_tiles();
            break;
        }
        case 'f':
        case 'F':
        {
            int z = static_cast<int>(pimpl->view.zoom_level()) - 1;
            pimpl->view.zoom_to_level(z);
            pimpl->rebuild_grid();
            pimpl->update_tiles();
            break;
        }
        }
    }
}

void map_widget::scroll_event(double, double yoffset)
{
    double factor = (yoffset > 0) ? 0.9 : 1.0 / 0.9;
    double wx = pimpl->cursor_ndc_x * 2.0 * pimpl->view.half_extent_y + pimpl->view.center_x;
    double wy = pimpl->cursor_ndc_y * 2.0 * pimpl->view.half_extent_y + pimpl->view.center_y;
    pimpl->view.zoom_at(factor, wx, wy);
    pimpl->rebuild_grid();
    pimpl->update_tiles();
}

void map_widget::framebuffer_size_event(int width, int height)
{
    if(height == 0) return;
    auto fwidth = static_cast<float>(width);
    auto fheight = static_cast<float>(height);
    pimpl->viewport_vec = glm::vec4(0.0F, 0.0F, fwidth, fheight);
    pimpl->normalized_viewport_width = fwidth / fheight;
    pimpl->projection_matrix = glm::orthoLH_ZO(
        -pimpl->normalized_viewport_width * 0.5F,
         pimpl->normalized_viewport_width * 0.5F,
        -0.5F, 0.5F, -1.0F, 1.0F);

    pimpl->view.viewport_width = width;
    pimpl->view.viewport_height = height;
    pimpl->rebuild_grid();
    pimpl->update_tiles();
}

bool map_widget::update()
{
    if(pimpl->tiles) pimpl->tiles->drain();

    bool new_candidates = pimpl->features.drain();
    if(new_candidates)
    {
        pimpl->labels.set_candidates(pimpl->features.labels());
    }

    // Reproject labels only when the view changed or new candidates arrived
    if(pimpl->needs_update || new_candidates)
    {
        pimpl->labels.update_positions(
            pimpl->view.center_x, pimpl->view.center_y,
            pimpl->view.half_extent_y,
            pimpl->view.viewport_width, pimpl->view.viewport_height,
            pimpl->vis);
    }

    bool result = pimpl->needs_update
        || (pimpl->tiles && pimpl->tiles->needs_upload())
        || pimpl->features.needs_upload() || pimpl->labels.needs_upload();

    if(result)
    {
        pimpl->needs_update = false;
    }
    return result;
}

std::vector<nasrbrowse::search_hit> map_widget::search(
    const std::string& query, int limit)
{
    return pimpl->pick_db.search(query, limit);
}

void map_widget::set_route_text(const std::string& text)
{
    if(text.empty())
    {
        clear_route();
        return;
    }
    // May throw route_parse_error; caller handles.
    pimpl->route.emplace(text, pimpl->pick_db);
    pimpl->features.set_route(pimpl->route);
    pimpl->needs_update = true;
    // Kick the feature builder so the route renders without waiting
    // for a pan/zoom to resubmit.
    pimpl->update_tiles();
}

void map_widget::clear_route()
{
    pimpl->route.reset();
    pimpl->features.set_route(std::nullopt);
    pimpl->needs_update = true;
    pimpl->update_tiles();
}

const std::optional<nasrbrowse::flight_route>& map_widget::route() const
{
    return pimpl->route;
}

void map_widget::render_frame(sdl::command_buffer& cmd, sdl::texture& swapchain)
{
    auto& d = *pimpl;
    auto& ctx = d.render_ctx;
    ctx.projection_matrix = d.projection_matrix;
    ctx.normalized_viewport_width = d.normalized_viewport_width;

    float s = static_cast<float>(1.0 / (2.0 * d.view.half_extent_y));
    float cx = static_cast<float>(d.view.center_x);
    float cy = static_cast<float>(d.view.center_y);
    glm::mat4 view_matrix = glm::scale(glm::mat4(1.0F), glm::vec3(s, s, 1.0F)) *
                             glm::translate(glm::mat4(1.0F), glm::vec3(-cx, -cy, 0.0F));

    // Copy phase: upload pending vertex/texture data to GPU
    {
        sdl::copy_pass copy(cmd);
        if(!d.grid_vertices.empty())
        {
            d.grid_buffer.reset();
            auto buf = copy.create_and_upload_buffer(d.dev, sdl::buffer_usage::vertex, d.grid_vertices);
            d.grid_buffer = std::make_unique<sdl::buffer>(std::move(buf));
        }
        if(d.tiles) d.tiles->copy(copy);
        d.features.copy(copy);
        d.labels.copy(copy, d.dev);
    }

    // Render phase: one render pass with 5 pipeline stages
    sdl::render_pass pass(cmd, swapchain, 0.1F, 0.1F, 0.12F, 1.0F);

    // Textured triangles (tile basemap)
    pass.bind_pipeline(d.textured_pipeline);
    ctx.current_pass = nasrbrowse::render_pass_id::textured_trianglelist_0;
    if(d.show_tiles && d.tiles)
        d.tiles->render(pass, ctx, view_matrix);

    // Polygon fills (airspace/SUA selection highlights)
    pass.bind_pipeline(d.trianglelist_pipeline);
    ctx.current_pass = nasrbrowse::render_pass_id::polygon_fill_0;
    d.features.render(pass, ctx, view_matrix);

    // Lines (grid overlay)
    pass.bind_pipeline(d.linelist_pipeline);
    ctx.current_pass = nasrbrowse::render_pass_id::trianglelist_0;
    if(d.grid_buffer && d.grid_buffer->count() > 0)
    {
        sdl::uniform_buffer uniforms;
        uniforms.projection_matrix = ctx.projection_matrix;
        pass.push_vertex_uniforms(0, &uniforms, sizeof(uniforms));
        pass.push_fragment_uniforms(0, &uniforms, sizeof(uniforms));
        pass.bind_vertex_buffer(*d.grid_buffer);
        pass.draw(d.grid_buffer->count());
    }

    // SDF lines (feature polylines)
    pass.bind_pipeline(d.line_sdf_pipeline);
    ctx.current_pass = nasrbrowse::render_pass_id::line_sdf_0;
    d.features.render(pass, ctx, view_matrix);

    // Text labels (reuse textured pipeline)
    pass.bind_pipeline(d.textured_pipeline);
    ctx.current_pass = nasrbrowse::render_pass_id::text_labels_0;
    d.labels.render(pass, ctx, d.text_sampler,
                     d.view.viewport_width, d.view.viewport_height);
}
