    // Shader data loading, from xxd-produced headers
    enum class shader_id
    {
        DEFAULT,
        LINE,
        TEXTURED
    };

    struct shader_bytecode
    {
        const unsigned char* data;
        unsigned int len;
    };

    // NOLINTBEGIN(cppcoreguidelines-pro-bounds-array-to-pointer-decay)
    // Shader bytecode is a generated C array that decays to a pointer
    // when stored in shader_bytecode.
    shader_bytecode get_spirv_bytecode(shader_id id, sdl::shader_stage_t stage)
    {
        if(stage == sdl::shader_stage::vertex)
        {
            switch(id)
            {
            case shader_id::DEFAULT:
                return {default_vert_spv, default_vert_spv_len};
            case shader_id::LINE:
                return {line_vert_spv, line_vert_spv_len};
            case shader_id::TEXTURED:
                return {textured_vert_spv, textured_vert_spv_len};
            }
        }
        else
        {
            switch(id)
            {
            case shader_id::DEFAULT:
                return {default_frag_spv, default_frag_spv_len};
            case shader_id::LINE:
                return {line_frag_spv, line_frag_spv_len};
            case shader_id::TEXTURED:
                return {textured_frag_spv, textured_frag_spv_len};
            }
        }
        return {nullptr, 0};
    }

