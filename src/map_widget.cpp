    void map_widget::focus_on_hit(const search_hit& hit)
    {
        auto bbox = pimpl->pick_db.get_hit_bbox(hit);
        if(!bbox)
        {
            return;
        }

        auto& d = *pimpl;
        auto cx = (bbox->lon_min + bbox->lon_max) * 0.5;
        auto cy = (bbox->lat_min + bbox->lat_max) * 0.5;
        d.view.center_x = lon_to_mx(cx);
        d.view.center_y = lat_to_my(cy);

        // Degenerate bbox (point entity) → use a fixed close-in zoom level so
        // the feature is guaranteed visible under chart_style's per-type
        // min_zoom thresholds (navaids appear by ~z9, airports by ~z7, fixes
        // by ~z10 — zoom 12 covers all of them comfortably).
        constexpr auto POINT_FOCUS_ZOOM = 12;
        auto is_point = (bbox->lon_min == bbox->lon_max && bbox->lat_min == bbox->lat_max);
        if(is_point)
        {
            d.view.zoom_to_level(POINT_FOCUS_ZOOM);
        }
        else
        {
            // Fit the bbox with 20% padding. Use the larger of the required
            // vertical / horizontal extents (the latter scaled by aspect ratio).
            auto half_height_m = (lat_to_my(bbox->lat_max) - lat_to_my(bbox->lat_min)) * 0.5;
            auto half_width_m = (lon_to_mx(bbox->lon_max) - lon_to_mx(bbox->lon_min)) * 0.5;
            auto needed = std::max(half_height_m, half_width_m / d.view.aspect_ratio());
            d.view.half_extent_y = needed * 1.2;
            // Force clamp by setting same extent through zoom(1.0).
            d.view.zoom(1.0);
        }

        d.needs_update = true;
        d.update_tiles();
    }

