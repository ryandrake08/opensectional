        void rebuild_grid()
        {
            grid_vertices.clear();

            auto vx_min = view.view_x_min();
            auto vx_max = view.view_x_max();
            auto vy_min = view.view_y_min();
            auto vy_max = view.view_y_max();
            auto range_x = vx_max - vx_min;
            auto range_y = vy_max - vy_min;

            // Normalize coordinates to -0.5..0.5 for rendering
            auto to_ndc_x = [&](double mx) -> float
            { return static_cast<float>(((mx - vx_min) / range_x - 0.5) * view.aspect_ratio()); };
            auto to_ndc_y = [&](double my) -> float { return static_cast<float>((my - vy_min) / range_y - 0.5); };

            // Draw grid lines at regular lat/lon intervals
            // Choose interval based on zoom level
            auto approx_zoom = view.zoom_level();
            double grid_step = 0.5;
            if(approx_zoom < 3)
            {
                grid_step = 30.0;
            }
            else if(approx_zoom < 5)
            {
                grid_step = 10.0;
            }
            else if(approx_zoom < 7)
            {
                grid_step = 5.0;
            }
            else if(approx_zoom < 9)
            {
                grid_step = 1.0;
            }
            auto lon_step = grid_step;
            auto lat_step = grid_step;

            // Grid line color: dark gray, semi-transparent
            uint8_t r = 80;
            uint8_t g = 80;
            uint8_t b = 80;
            uint8_t a = 160;

            // Longitude lines (vertical)
            auto lon_min = mx_to_lon(vx_min);
            auto lon_max = mx_to_lon(vx_max);
            auto lon_start = std::floor(lon_min / lon_step) * lon_step;
            auto lon_count = static_cast<int>((lon_max - lon_start) / lon_step) + 1;
            for(int i = 0; i < lon_count; ++i)
            {
                auto lon = lon_start + i * lon_step;
                auto mx = lon_to_mx(lon);
                auto nx = to_ndc_x(mx);
                auto ny0 = to_ndc_y(vy_min);
                auto ny1 = to_ndc_y(vy_max);
                grid_vertices.push_back({0, 0, r, g, b, a, nx, ny0, 0});
                grid_vertices.push_back({0, 0, r, g, b, a, nx, ny1, 0});
            }

            // Latitude lines (horizontal, non-uniform spacing in Mercator)
            auto lat_min = my_to_lat(vy_min);
            auto lat_max = my_to_lat(vy_max);
            auto lat_start = std::floor(lat_min / lat_step) * lat_step;
            auto lat_count = static_cast<int>((lat_max - lat_start) / lat_step) + 1;
            for(int i = 0; i < lat_count; ++i)
            {
                auto lat = lat_start + i * lat_step;
                auto my = lat_to_my(lat);
                auto ny = to_ndc_y(my);
                auto nx0 = to_ndc_x(vx_min);
                auto nx1 = to_ndc_x(vx_max);
                grid_vertices.push_back({0, 0, r, g, b, a, nx0, ny, 0});
                grid_vertices.push_back({0, 0, r, g, b, a, nx1, ny, 0});
            }

            needs_update = true;
        }

